<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				tuifangbianhao : false,
				jiudianmingcheng : false,
				jiudianleibie : false,
				tupian : false,
				fangjianleixing : false,
				tuifangshijian : false,
				yonghuzhanghao : false,
				yonghuxingming : false,
				dianhuahaoma : false,
				shenfenzheng : false,
				guanlizhanghao : false,
				guanlixingming : false,
				sfsh : false,
				shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          tuifangbianhao: this.getUUID(),
          jiudianmingcheng: '',
          jiudianleibie: '',
          tupian: '',
          fangjianleixing: '',
          tuifangshijian: '',
          yonghuzhanghao: '',
          yonghuxingming: '',
          dianhuahaoma: '',
          shenfenzheng: '',
          guanlizhanghao: '',
          guanlixingming: '',
        },


        rules: {
          tuifangbianhao: [
          ],
          jiudianmingcheng: [
          ],
          jiudianleibie: [
          ],
          tupian: [
          ],
          fangjianleixing: [
          ],
          tuifangshijian: [
          ],
          yonghuzhanghao: [
          ],
          yonghuxingming: [
          ],
          dianhuahaoma: [
          ],
          shenfenzheng: [
          ],
          guanlizhanghao: [
          ],
          guanlixingming: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.tuifangshijian = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='tuifangbianhao'){
              this.ruleForm.tuifangbianhao = obj[o];
              this.ro.tuifangbianhao = true;
              continue;
            }
            if(o=='jiudianmingcheng'){
              this.ruleForm.jiudianmingcheng = obj[o];
              this.ro.jiudianmingcheng = true;
              continue;
            }
            if(o=='jiudianleibie'){
              this.ruleForm.jiudianleibie = obj[o];
              this.ro.jiudianleibie = true;
              continue;
            }
            if(o=='tupian'){
              this.ruleForm.tupian = obj[o].split(",")[0];
              this.ro.tupian = true;
              continue;
            }
            if(o=='fangjianleixing'){
              this.ruleForm.fangjianleixing = obj[o];
              this.ro.fangjianleixing = true;
              continue;
            }
            if(o=='tuifangshijian'){
              this.ruleForm.tuifangshijian = obj[o];
              this.ro.tuifangshijian = true;
              continue;
            }
            if(o=='yonghuzhanghao'){
              this.ruleForm.yonghuzhanghao = obj[o];
              this.ro.yonghuzhanghao = true;
              continue;
            }
            if(o=='yonghuxingming'){
              this.ruleForm.yonghuxingming = obj[o];
              this.ro.yonghuxingming = true;
              continue;
            }
            if(o=='dianhuahaoma'){
              this.ruleForm.dianhuahaoma = obj[o];
              this.ro.dianhuahaoma = true;
              continue;
            }
            if(o=='shenfenzheng'){
              this.ruleForm.shenfenzheng = obj[o];
              this.ro.shenfenzheng = true;
              continue;
            }
            if(o=='guanlizhanghao'){
              this.ruleForm.guanlizhanghao = obj[o];
              this.ro.guanlizhanghao = true;
              continue;
            }
            if(o=='guanlixingming'){
              this.ruleForm.guanlixingming = obj[o];
              this.ro.guanlixingming = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.yonghuzhanghao!=''&&json.yonghuzhanghao) || json.yonghuzhanghao==0){
                this.ruleForm.yonghuzhanghao = json.yonghuzhanghao;
				this.ro.yonghuzhanghao = true;
            }
            if((json.yonghuxingming!=''&&json.yonghuxingming) || json.yonghuxingming==0){
                this.ruleForm.yonghuxingming = json.yonghuxingming;
				this.ro.yonghuxingming = true;
            }
            if((json.dianhuahaoma!=''&&json.dianhuahaoma) || json.dianhuahaoma==0){
                this.ruleForm.dianhuahaoma = json.dianhuahaoma;
				this.ro.dianhuahaoma = true;
            }
            if((json.shenfenzheng!=''&&json.shenfenzheng) || json.shenfenzheng==0){
                this.ruleForm.shenfenzheng = json.shenfenzheng;
				this.ro.shenfenzheng = true;
            }
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`tuifangxinxi/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {
			if(this.ruleForm.tuifangbianhao){
				this.ruleForm.tuifangbianhao = String(this.ruleForm.tuifangbianhao)
			}
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('tuifangxinxi/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`tuifangxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`tuifangxinxi/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      tupianUploadChange(fileUrls) {
          this.ruleForm.tupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");
      },
    }
  };
</script>

